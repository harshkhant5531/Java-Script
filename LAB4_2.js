/*Pop*/
const fruits1 = ["Banana", "Orange", "Apple", "Mango"];
fruits1.pop();
console.log(fruits1)

/*Push*/
const fruits2 = ["Banana", "Orange", "Apple", "Mango"];
fruits2.push("Kiwi");
console.log(fruits2)

/*Shift*/
const fruits3 = ["Banana", "Orange", "Apple", "Mango"];
fruits3.shift();
console.log(fruits3)

/*Unshift*/
const fruits4 = ["Banana", "Orange", "Apple", "Mango"];
fruits4.unshift("Lemon");
console.log(fruits4)